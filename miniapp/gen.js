// 所有数据用 JS 模板字面量（反引号）包裹，避免引号冲突
function j(str) {
  // 将字符串转为安全的 JS 表达式（用反引号模板字面量）
  return '`' + String(str).replace(/`/g, '\\`').replace(/\$/g, '\\$') + '`';
}

function makeKP(grade, subject, textbook, chapter, title, type, content) {
  return `  { grade: '${grade}', subject: '${subject}', textbook: '${textbook}', chapter: '${chapter}', title: '${title}', type: '${type||'基础'}', content: ${j(content||'')}, mastery: 0 }`;
}

function makeQ(grade, subject, textbook, chapter, difficulty, question, options, answer, explanation) {
  const optsStr = '[' + options.map(o => `'${o}'`).join(', ') + ']';
  return `  { grade: '${grade}', subject: '${subject}', textbook: '${textbook}', chapter: '${chapter}', difficulty: '${difficulty}', question: ${j(question)}, options: ${optsStr}, answer: '${answer}', explanation: ${j(explanation||'')} }`;
}

let kpLines = [];
let qLines = [];
let kpId = 1;
let qId = 1;

function kp(g, s, t, c, title, type, content) {
  kpLines.push(`  // ${g} ${s} ${t} ${c}\n${makeKP(g, s, t, c, title, type, content)}`);
  kpId++;
}
function q(g, s, t, c, diff, qText, opts, ans, expl) {
  qLines.push(`  // ${g} ${s} ${c}\n${makeQ(g, s, t, c, diff, qText, opts, ans, expl)}`);
  qId++;
}

