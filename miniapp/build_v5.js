// ============ 生成知识点 ============
// 为每个章节生成知识点

function genKPForChapter(grade, subject, struct, count) {
  const info = struct[grade];
  if (!info) return;
  const { tb, ch } = info;
  for (const chapter of ch) {
    const types = ['基础', '重点', '考点'];
    for (let i = 0; i < count; i++) {
      addKP(grade, subject, tb, chapter, chapter + ' 知识点' + (i+1), types[i % 3], chapter + '的核心学习内容，需要认真掌握');
    }
  }
}

