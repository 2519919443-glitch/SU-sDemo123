// ============ 辅助函数 ============
function makeId(prefix, num) {
  return prefix + '_' + String(num).padStart(5, '0');
}

// ============ 全局数据存储 ============
let kpId = 1;
let qId = 1;
const knowledgePoints = [];
const quizBanks = [];

function addKP(grade, subject, textbook, chapter, title, type, content) {
  knowledgePoints.push({
    id: makeId('kp', kpId++),
    grade: String(grade),
    subject,
    textbook,
    chapter,
    title,
    type, // 基础/重点/考点
    content,
    mastery: 0,
  });
}

function addQ(grade, subject, textbook, chapter, difficulty, question, options, answer, explanation) {
  quizBanks.push({
    id: makeId('q', qId++),
    grade: String(grade),
    subject,
    textbook,
    chapter,
    difficulty, // 易/中/难
    question,
    options,
    answer,
    explanation,
  });
}

